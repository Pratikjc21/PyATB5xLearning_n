#Input
Num_1= int(input("Enter num 1: "))
Num_2= int(input("Enter num 2: "))
Num_3= int(input("Enter num 3: "))

#Logic
Sum= int(Num_1+Num_2+Num_3)
Sub= int(Num_1-Num_2-Num_3)
Mul= int(Num_1*Num_2*Num_3)
Div= float(Num_1/Num_2/Num_3)

#Output
print("Sum of 3 numbers is",Sum)
print("Sub of 3 numbers is",Sub)
print("Mul of 3 numbers is",Mul)
print("Div of 3 numbers is",Div)


