# Input

Num_1 = float(input("Enter num 1: "))
Num_2 = float(input("Enter num 2: "))

# Logic
Div = float(Num_1 / Num_2)
Quotient = int(Num_1 // Num_2)
Remainder = int(Num_1 % Num_2)

# Output
print("Div of 2 numbers is", Div)
print("Rem of 2 numbers is", Remainder)
print("Quot of 2 numbers is", Quotient)
